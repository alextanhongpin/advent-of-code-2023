func draw(static map[Point]rune) string {
	var maxX, maxY int
	for p := range static {
		maxX = max(maxX, p.x)
		maxY = max(maxY, p.y)
	}

	var rows []string
	for y := 0; y <= maxY; y++ {
		var cols []string
		for x := 0; x <= maxX; x++ {
			if c, ok := static[Point{x, y}]; ok {
				cols = append(cols, string(c))
			} else {
				cols = append(cols, string('.'))
			}
		}
		rows = append(rows, strings.Join(cols, ""))
	}

	return strings.Join(rows, "\n")
}

type Point struct {
	x, y int
}

func cmp(a, b int) int {
	if a < b {
		return -1
	}
	if a > b {
		return 1
	}
	return 0
}

func solve(in string, n int, cycle int) int {
	target := n
	dir := "uldr"

	var a, b string
	seen := make([]string, 50)
	for n > 0 {
		for i := 0; i < cycle; i++ {
			d := dir[i%len(dir)]
			grid, static, balls := parse(in)

			slices.SortFunc(balls, func(a, b Point) int {
				switch d {
				case 'u':
					// top to bottom, left to right
					if a.y == b.y {
						return cmp(a.x, b.x)
					}
					return cmp(a.y, b.y)
				case 'd':
					if a.y == b.y {
						return cmp(a.x, b.x)
					}
					// down first, then up
					return cmp(b.y, a.y)
				case 'l':
					// left first, then right
					if a.y == b.y {
						return cmp(a.x, b.x)
					}
					return cmp(a.y, b.y)
				case 'r':
					if a.y == b.y {
						// Right first, then left
						return cmp(b.x, a.x)
					}
					return cmp(a.y, b.y)
				}

				return 0
			})
			for len(balls) > 0 {
				//draw(static)
				var keep []Point
				for _, ball := range balls {
					x := ball.x
					y := ball.y
					switch d {
					case 'u':
						y--
					case 'd':
						y++
					case 'l':
						x--
					case 'r':
						x++
					}
					pos := Point{x, y}

					if !grid[pos] {
						static[ball] = 'O'
						continue
					}
					// Is there something else there?
					if _, ok := static[pos]; ok {
						static[ball] = 'O'
						continue
					}
					keep = append(keep, pos)
				}
				balls = keep
			}
			newin := draw(static)

			if i == cycle-1 {
				n--
			}

			var maxY int
			for p := range grid {
				maxY = max(maxY, p.y)
			}
			maxY += 1

			var result int
			for p, c := range static {
				if c != 'O' {
					continue
				}
				load := maxY - p.y
				result += load
			}

			key := (n + 1) % len(seen)
			if (n+1)%len(seen) == 0 {
				if a == "" {
					a = strings.Join(seen, ",")
				} else {
					b = strings.Join(seen, ",")
					var count int
					for i := 0; i < len(a); i++ {
						if a[i] != b[i] {

							break
						}
						count++
					}

					_ = target
					//m := target - (n + 1)
					//left := m / len(seen)
					//m -= left * len(seen)
					//n = target - m
					a = b
					b = ""
				}
				clear(seen)
				// Reset.
			}
			seen[key] = fmt.Sprint(result)
			if i == 3 {
				fmt.Println(n+1, result, i, seen)
			}
			if n == 0 && i == cycle-1 {
				return result
			}

			in = newin
		}
	}

	panic("invalid")
}

func parse(in string) (map[Point]bool, map[Point]rune, []Point) {
	var points []Point
	grid := make(map[Point]bool)
	static := make(map[Point]rune)
	for y, line := range strings.Split(in, "\n") {
		for x, r := range line {
			p := Point{x, y}
			if r == '#' {
				static[p] = r
			}
			grid[p] = true
			if r == 'O' {
				points = append(points, p)
			}
		}
	}
	return grid, static, points
}

