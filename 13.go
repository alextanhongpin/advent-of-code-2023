func solve(in string, fix bool) int {
	var result int

	sections := strings.Split(in, "\n\n")
	for _, s := range sections {
		v := parse(s)
		if fix {
			result += fixSmudge(v)
		} else {
			n, ok := getReflect(v)
			if ok {
				result += n
			}
		}
	}
	return result
}

type Point struct {
	x, y int
}

type Range struct {
	min, max int
	dir      string
}

func fixSmudge(grid map[Point]bool) int {
	var maxX, maxY int
	for p := range grid {
		maxX = max(maxX, p.x)
		maxY = max(maxY, p.y)
	}

	var result = math.MaxInt
	for y := 0; y <= maxY; y++ {
		for x := 0; x <= maxX; x++ {
			newGrid := maps.Clone(grid)
			p := Point{x, y}
			if newGrid[p] {
				delete(newGrid, p)
			} else {
				newGrid[p] = true
			}
			if n, ok := getReflect(newGrid); ok {
				result = min(result, n)
			}
		}
	}
	return result
}

func getReflect(grid map[Point]bool) (int, bool) {
	var maxX, maxY int
	for p := range grid {
		maxX = max(maxX, p.x)
		maxY = max(maxY, p.y)
	}

	cache := make(map[string]int)
	cacheByY := make(map[int]string)
	cacheByX := make(map[int]string)
	var rngs []Range

	// Find horizontal (y-min, y-max) reflection.
	for y := 0; y <= maxY; y++ {
		var xs []string
		for x := 0; x <= maxX; x++ {
			if grid[Point{x, y}] {
				xs = append(xs, fmt.Sprint(x))
			}
		}
		key := strings.Join(xs, ",")
		cacheByY[y] = key
		if yPrev, ok := cache[key]; ok && y == yPrev+1 {
			rngs = append(rngs, Range{yPrev, y, "horizontal"})
		}
		cache[key] = y
	}

	clear(cache)
	// Find vertical (x-min, x-max) reflection.
	for x := 0; x <= maxX; x++ {
		var ys []string
		for y := 0; y <= maxY; y++ {
			if grid[Point{x, y}] {
				ys = append(ys, fmt.Sprint(y))
			}
		}
		key := strings.Join(ys, ",")
		cacheByX[x] = key
		if xPrev, ok := cache[key]; ok && x == xPrev+1 {
			rngs = append(rngs, Range{xPrev, x, "vertical"})
		}
		cache[key] = x
	}

	for _, r := range rngs {
		if r.dir == "horizontal" {
			var valid = true
			dy := min(r.min, maxY-r.max)
			for y := 0; y <= dy; y++ {
				prev := cacheByY[r.min-y]
				curr := cacheByY[r.max+y]
				if prev != curr {
					valid = false
					break
				}

			}
			if valid {
				return r.max * 100, true
			}
			continue
		}

		var valid = true
		dx := min(r.min, maxX-r.max)
		for x := 0; x <= dx; x++ {
			prev := cacheByX[r.min-x]
			curr := cacheByX[r.max+x]
			if prev != curr {
				valid = false
				break
			}

		}
		if valid {
			return r.max, true
		}
	}
	return 0, false
}

func parse(in string) map[Point]bool {
	lines := strings.Split(in, "\n")
	res := make(map[Point]bool)
	for y, line := range lines {
		for x, c := range line {
			if c != '#' {
				continue
			}

			res[Point{x, y}] = true
		}
	}
	return res
}

